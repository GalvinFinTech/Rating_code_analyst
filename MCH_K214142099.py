def calculate_rsi(data, window=14):
    delta = data.diff()
    up = delta.mask(delta < 0, 0)
    down = -delta.mask(delta > 0, 0)

    avg_gain = up.rolling(window).mean()
    avg_loss = down.rolling(window).mean()

    rs = avg_gain / avg_loss
    rsi = 100 - (100 / (1 + rs))
    return rsi
